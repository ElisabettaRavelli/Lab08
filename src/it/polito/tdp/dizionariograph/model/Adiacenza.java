package it.polito.tdp.dizionariograph.model;

public class Adiacenza {
	
	private String w1;
	private String w2;
	
	public Adiacenza(String w1, String w2) {
		super();
		this.w1 = w1;
		this.w2 = w2;
	}
	public String getW1() {
		return w1;
	}
	public void setW1(String w1) {
		this.w1 = w1;
	}
	public String getW2() {
		return w2;
	}
	public void setW2(String w2) {
		this.w2 = w2;
	}
	
	

}
